# Operators in Python

# Addition (+)
# Subtraction (-)
# Multiplication (*)
# Division (/)
# Exponentiation (**)
# Modulus (%)

x = 10
y = 2

print(x+y)  # Addition prints 12
print(x-y)  # Subtraction prints 8
print(x*y)  # Multiplication prints 20
print(x/y)  # Division pints 5.0
print(x**y)  # Exponentiation prints
print(x%y)  # Modulus prints 0

z = 10
z += 5  # Increment z by 5
print(z)  # prints 15
